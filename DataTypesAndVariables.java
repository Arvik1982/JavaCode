public class DataTypesAndVariables {
    public static void main(String[] args) {
        var age = 12;
        System.out.println(age);
        /**
         * Числовые типы
         * 
         * Целые числа
         * 
         * byte byteVar = 127;
         * short shortVar = 32767;
         * int intVar = 2147483647;
         * long longVar = 9223372036854775807L;
         * 
         * Вещественные числа (с плавающей точкой)
         * 
         * float floatVar = 3.14f;
         * double doubleVar = 3.14159265359;
         *
         * Текстовые типы
         * 
         * Строки (в двойных кавычках)
         * 
         * String str = "Это строка";
         * String multiStr = "Многострочная\nстрока";
         * 
         * Символы (в одинарных кавычках)
         * 
         * char charVar = 'A';
         * boolean boolVar = true;
         * 
         * Логический тип
         * 
         * Используется для результатов сравнений
         * Работает с операторами сравнения (>, <, >=, <=, ==, !=)
         * Поддерживает логические операции (AND, OR)
         */
    }
}
